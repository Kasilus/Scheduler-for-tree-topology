public class CriticalPathInfoForTask {

    private int verticesOnCriticalPath;
    private int timesOnCriticalPath;

    public int getVerticesOnCriticalPath() {
        return verticesOnCriticalPath;
    }

    public void setVerticesOnCriticalPath(int verticesOnCriticalPath) {
        this.verticesOnCriticalPath = verticesOnCriticalPath;
    }

    public int getTimesOnCriticalPath() {
        return timesOnCriticalPath;
    }

    public void setTimesOnCriticalPath(int timesOnCriticalPath) {
        this.timesOnCriticalPath = timesOnCriticalPath;
    }
}
